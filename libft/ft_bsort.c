#include "libft.h"

void	ft_bsort(int *c, int size)
{
	int i;

	i = 0;
	while (i < size - 1)
	{
		if (c[i] > c[i + 1])
		{
			ft_swap((c + i), (c + i + 1));
			i = 0;
		}
		else
			i++;
	}
}
